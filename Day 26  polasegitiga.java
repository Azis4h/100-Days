package pola.segitiga;
public class PolaSegitiga {
    public static void main(String[] args) {
       int pola4 = 5;
        for (int i = 0; i < pola4; i++) {
            for (int j = 0; j <= i; j++) {
                System.out.print("*");
            }
            System.out.println("");
        }


    }
}
