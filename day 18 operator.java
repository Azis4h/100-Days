package operator.java;
public class OperatorJava {
    public static void main(String[] args) {

        long a = 21, d = 2;
        long h = a%d;
        long y = d*a;
        
        float s = 19.2f, u = 3.2f;
        float g =  s/u;
        float r = u+s;
        
        System.out.println(h);
        System.out.println(y);
        System.out.println(g);
        System.out.println(r);
    }
    
}
